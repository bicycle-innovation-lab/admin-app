<template>
  <div>
    <input type="file" @change="onfileSelected">
    <v-btn @click="uploadFile">Upload</v-btn>

    <v-divider></v-divider>

<form id="file-upload-form" class="uploader">
  <input id="file-upload" type="file" name="fileUpload" accept="image/*" />

  <label for="file-upload" id="file-drag">
    <img id="file-image" src="#" alt="Preview" class="hidden">
    <div id="start">
      <i class="fa fa-download" aria-hidden="true"></i>
      <div>Select a file or drag here</div>
      <div id="notimage" class="hidden">Please select an image</div>
      <v-btn id="file-upload-btn">Select a file</v-btn>
    </div>
    <div id="response" class="hidden">
      <div id="messages"></div>
      <progress class="progress" id="file-progress" value="0">
        <span>0</span>%
      </progress>
    </div>
  </label>

  

</form>
  </div>
</template>

<script>
import ImageService from '@/services/image.service'
import Compressor from 'compressorjs'

export default {
  name: 'ImageUpload',

  data () {
    return {
      selectedFile: null
    }
  },

  methods: {
    onfileSelected(event) {
      this.selectedFile = event.target.files[0]
    },
    uploadFile() {
      if (this.selectedFile === null) {
        return
      }
      
      const imageService = new ImageService

      // Initial compression to make sure we don't upload huge images.
      new Compressor(this.selectedFile, {
        // default quality
        quality: 0.8,
        // Ipad retina screensize
        maxWidth: 2732,
        maxHeight: 2048,
        success(result){
          const formData = new FormData()
          formData.append('file', result, result.name)
          formData.set('title', 'En titel til billedet')
          formData.set('alt', 'En titel til billedet')

          imageService.create(formData)
            .then(() => {
              // eslint-disable-next-line
              console.log('Upload success');
            })
        },
        error(error) {
          // eslint-disable-next-line
          console.log(error.message)
        }
      })
    }
  }
}
</script>

